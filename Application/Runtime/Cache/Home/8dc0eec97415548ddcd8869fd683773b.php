    function getVerify() {
        phone = $('#phone').val();
        $.post('<?php echo U("sendVerifyCode");?>',{phone:phone},function (rs) {
            if(rs){
                rs = JSON.parse(rs);
                if(rs['errno'] == 1){
                    alert('验证码发送成功');
                }else {
                    alert(rs['msg']);
                }
            }else {
                alert('验证码发送失败');
            }
        })
        return false;
    }

    function register() {
        var pwd = $('#password').val();
        var pwd1 = $('#password1').val();
        var phone = $('#phone').val();
        var verify = $('#verify').val();
        var nickname = $('#nickname').val();
        var companyname = $('#companyname').val();
        var province = $('#province').find('option:selected').text();
        var city = $('#city').find('option:selected').text();
        var area = $('#area').find('option:selected').text();
        var address = $('#address').val();
        var email = $('#email').val();
        var businesspic = $('#businesspic').val();
        var cardpic1 = $('#cardpic1').val();
        var cardpic2 = $('#cardpic2').val();

		if($('#province').find('option:selected').val() == 0 || $('#city').find('option:selected').val() == 0 || $('#area').find('option:selected').val() == 0 || $('#address').val() == ''){
		    alert('地址必须填写');
		    return;
		}
		if(businesspic == ''){
		    alert('请上传营业执照');
		    return;
		}
        if(cardpic1 == '' || cardpic2 == ''){
            alert('请上传身份证照');
            return;
        }
        if(!$('#xieyi').is(':checked')){
            alert('用户注册协议必须同意');
            return;
        }
        if(pwd.length < 6 || pwd.length > 16){
            alert('密码名必须大于6位小于16位');
            return;
        }
        if(pwd != pwd1){
            alert('2次密码输入不一致');
            return;
        }

        var data = {
		    password:pwd,
			password1:pwd1,
			phone:phone,
			verify:verify,
			nickname:nickname,
			companyname:companyname,
			province:province,
			city:city,
			area:area,
			address:address,
			email:email,
			businesspic:businesspic,
			cardpic1:cardpic1,
			cardpic2:cardpic2,
		};
        $.post('<?php echo U("businessRegister");?>',data,function(rs){
            if(rs){
                rs = JSON.parse(rs);
                if(rs['errno'] == 1){
                    location.href = '<?php echo U(businessSuccess);?>';
                }else {
                    alert(rs['msg']);
                }
            }else {
                alert('注册失败');
            }
        })
    }
